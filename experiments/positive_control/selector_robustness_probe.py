ROOT = Path(__file__).resolve().parents[2]
RESULTS_PATH = ROOT / "docs" / "selector_robustness_probe_results.json"
FROZEN = ROOT / "docs" / "prereg" / "frozen"

N_ELEMENTS = 600
# frozen pipeline constants (p3_dynamics.py / p6 gates); the selector spec
# (chain_count, min_length) is the only quantity this probe varies
MAX_TARGETS = 44
MIN_TARGETS = 20
TRAIN_C = 3000
HELDOUT_C = 800
STEPS = 1500
RESTARTS = 5
MIN_COLS = 4
TRUTH_COMPARISONS = 8000
GATE_HELDOUT = 0.10
GATE_NULL_GAP = 0.10
GATE_TRUTH = 0.40

UNIFORM_SEEDS = range(200, 220)
LAYERED_SEEDS = range(100, 105)
LAYER_JITTER_WINDOW = 60

# (name, selector kind, chain_count, min_length, selector_seed)
VARIANTS: tuple[tuple[str, str, int, int, int | None], ...] = (
    ("greedy_6x25_frozen", "greedy", 6, 25, None),
    ("greedy_4x25", "greedy", 4, 25, None),
    ("greedy_8x25", "greedy", 8, 25, None),
    ("greedy_6x20", "greedy", 6, 20, None),
    ("greedy_6x30", "greedy", 6, 30, None),
    ("anchored_6x25_s0", "anchored", 6, 25, 0),
    ("anchored_6x25_s1", "anchored", 6, 25, 1),
    ("anchored_6x25_s2", "anchored", 6, 25, 2),
)
FROZEN_VARIANT = "greedy_6x25_frozen"


def _depth_pred(
    causal: np.ndarray, topo: np.ndarray, allowed: np.ndarray
) -> tuple[np.ndarray, np.ndarray]:
    """Longest-chain DP restricted to ``allowed`` (depth and predecessor)."""

    depth = np.where(allowed, 1, 0).astype(int)
    pred = np.full(causal.shape[0], -1, dtype=int)
    for u in topo:
        if not allowed[u]:
            continue
        succ = np.flatnonzero(causal[u] & allowed)
        improve = succ[depth[u] + 1 > depth[succ]]
        depth[improve] = depth[u] + 1
        pred[improve] = u
    return depth, pred


def select_anchored_chains(
    causal: np.ndarray,
    times: np.ndarray,
    chain_count: int,
    min_length: int,
    seed: int,
) -> list[np.ndarray]:
    """Non-greedy selector: longest chain THROUGH a random anchor element.

    Per round, two DPs give the longest chain ending at and starting from
    every remaining element; an anchor is drawn uniformly from elements
    whose through-chain meets the tick threshold, and that chain is taken.
    Unlike the frozen greedy rule this never consults the global maximum,
    so chain placement is randomized by the selector seed.
    """

    rng = np.random.default_rng(seed)
    topo = np.argsort(times)
    allowed = np.ones(causal.shape[0], dtype=bool)
    chains: list[np.ndarray] = []
    for _ in range(chain_count):
        depth_in, pred_in = _depth_pred(causal, topo, allowed)
        depth_out, pred_out = _depth_pred(causal.T, topo[::-1], allowed)
        through = np.where(allowed, depth_in + depth_out - 1, 0)
        eligible = np.flatnonzero(through >= min_length)
        if eligible.size == 0:
            break
        anchor = int(rng.choice(eligible))
        past: list[int] = []
        node = anchor
        while node != -1:
            past.append(node)
            node = int(pred_in[node])
        future: list[int] = []
        node = int(pred_out[anchor])
        while node != -1:
            future.append(node)
            node = int(pred_out[node])
        chain = np.array(past[::-1] + future, dtype=int)
        chains.append(chain)
        allowed[chain] = False
    return chains


def _select(variant: tuple[str, str, int, int, int | None], causal, times):
    _, kind, chain_count, min_length, selector_seed = variant
    if kind == "greedy":
        return select_disjoint_chains(causal, times, chain_count, min_length)
    assert selector_seed is not None
    return select_anchored_chains(
        causal, times, chain_count, min_length, selector_seed
    )


def _fit_heldout(profiles: EchoProfileMatrix, seed: int):
    diss = profile_dissimilarity_matrix(profiles, MIN_COLS)
    margin = margin_from_probe_quantile(diss, seed=seed + 3)
    split = build_constraint_split(diss, TRAIN_C, HELDOUT_C, margin, seed=seed + 5)
    coords, _ = fit_ordinal_embedding_gradient_descent(
        profiles.target_count, 1, split.train, steps=STEPS,
        learning_rate=0.05, seed=seed + 100, restarts=RESTARTS,
    )
    return coords, quadruplet_violation_rate(coords, split.heldout)


def _column_shuffle(profiles: EchoProfileMatrix, seed: int) -> EchoProfileMatrix:
    rng = np.random.default_rng(seed)
    delays = profiles.delay_ranks.copy()
    reach = profiles.reachable.copy()
    for col in range(profiles.reference_count):
        perm = rng.permutation(profiles.target_count)
        delays[:, col] = delays[perm, col]
        reach[:, col] = reach[perm, col]
    return EchoProfileMatrix(delays, reach, profiles.target_indices.copy())


def evaluate(
    causal: np.ndarray,
    times: np.ndarray,
    coords: np.ndarray,
    variant: tuple[str, str, int, int, int | None],
    seed: int,
) -> dict:
    """One instrument evaluation with the frozen pipeline and a swapped
    chain-selection stage (mirrors p3_dynamics.analyze_order otherwise)."""

    chain_count = variant[2]
    chains = _select(variant, causal, times)
    if len(chains) < chain_count:
        return {"status": f"structural_block: only {len(chains)} chains"}
    targets = select_bracketed_targets(causal, chains, MAX_TARGETS, seed)
    if targets.size < MIN_TARGETS:
        return {"status": f"structural_block: {targets.size} targets"}
    profiles = measure_order_intrinsic_profiles(causal, chains, targets)
    try:
        coords_fit, heldout = _fit_heldout(profiles, seed)
        _, null_heldout = _fit_heldout(_column_shuffle(profiles, seed + 61), seed)
    except ValueError as error:
        return {"status": f"structural_block: {str(error)[:40]}"}
    null_gap = float(null_heldout - heldout)
    truth = float(
        embedding_distance_order_error(
            coords_fit, coords[targets].reshape(-1, 1),
            num_pair_comparisons=TRUTH_COMPARISONS, seed=seed + 9,
        )
    )
    margin = minimum_gate_margin(
        heldout=float(heldout), heldout_max=GATE_HELDOUT,
        null_gap=null_gap, null_gap_min=GATE_NULL_GAP,
        truth_error=truth, truth_error_max=GATE_TRUTH,
    )
    return {
        "status": "ok",
        "n_targets": int(targets.size),
        "n_chains": len(chains),
        "min_chain_len": int(min(c.size for c in chains)),
        "heldout": float(heldout),
        "null_gap": null_gap,
        "truth": truth,
        "gate_pass": bool(margin >= 0.0),
        "G": geometry_order_parameter(
            status="ok", heldout=float(heldout),
            null_gap=null_gap, truth_error=truth,
        ),
    }


def order_inputs(pi: np.ndarray) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    idx = np.arange(pi.size, dtype=float)
    return perm_to_causal_matrix(pi), idx + pi, idx - pi


def uniform_orders() -> list[tuple[str, np.ndarray, int]]:
    return [
        (
            f"uniform_seed{seed}",
            np.random.default_rng(seed).permutation(N_ELEMENTS),
            seed,
        )
        for seed in UNIFORM_SEEDS
    ]


def layered_orders() -> list[tuple[str, np.ndarray, int]]:
    cells = json.loads((FROZEN / "p6_test_constants.json").read_text())[
        "layered_cells"
    ]
    orders = []
    for cell in cells:
        k, moves = int(cell["layer_count"]), int(cell["moves"])
        for seed in LAYERED_SEEDS:
            base = balanced_layered_perm(
                N_ELEMENTS, layer_count=k, seed=seed, min_layer_size=6
            )
            pi = windowed_transpositions(
                base, moves=moves, window=LAYER_JITTER_WINDOW, seed=10_000 + seed
            )
            orders.append((f"layered_k{k}_m{moves}_seed{seed}", pi, seed))
    return orders


def p5_orders() -> list[tuple[str, np.ndarray, int]]:
    """Regenerate the frozen P5 continuum samples (provenance-checked)."""

    from causal_spacetime_lab.positive_control.accelerated_two_orders import (
        mcmc_2d_order_replay_accelerated,
    )

    constants = json.loads((FROZEN / "p5_test_constants.json").read_text())
    with (FROZEN / "p5_stage_b_all.csv").open(newline="", encoding="utf-8") as fh:
        frozen_rows = list(csv.DictReader(fh))
    orders = []
    for beta in (2.0, 8.0):
        for seed in (100, 101, 102):
            pi0 = np.random.default_rng(
                seed * 31 + int(round(beta * 10))
            ).permutation(N_ELEMENTS)
            samples, _, perms = mcmc_2d_order_replay_accelerated(
                pi0,
                beta=beta,
                eps=float(constants["eps"]),
                steps=int(constants["steps_continuum"]),
                seed=seed * 1000 + int(round(beta * 10)),
                sample_every=int(constants["sample_every_continuum"]),
                burn_frac=float(constants["burn_frac"]),
                collect_perms=True,
            )
            chain_s = float(np.mean([s["S"] for s in samples]))
            expected = [
                float(r["chain_S"])
                for r in frozen_rows
                if r.get("beta") == f"{beta:.1f}" and r.get("seed") == str(seed)
            ]
            if not expected or abs(chain_s - expected[0]) > 1e-9:
                raise RuntimeError(
                    f"P5 replay provenance mismatch at beta={beta:g} seed={seed}: "
                    f"{chain_s} vs {expected[:1]}"
                )
            orders.extend(
                (f"p5_b{beta:g}_s{seed}_sample{i}", pi, seed * 10 + i)
                for i, pi in enumerate(perms)
            )
            print(f"  replayed P5 beta={beta:g} seed={seed}: {len(perms)} samples")
    return orders


def scan_family(name: str, orders: list[tuple[str, np.ndarray, int]]) -> dict:
    per_variant: dict[str, dict] = {}
    for variant in VARIANTS:
        rows = []
        for order_name, pi, seed in orders:
            causal, times, coords = order_inputs(pi)
            result = evaluate(causal, times, coords, variant, seed)
            rows.append({"order": order_name, **result})
        ok = [r for r in rows if r["status"] == "ok"]
        passes = sum(1 for r in ok if r["gate_pass"])
        summary = {
            "orders": len(rows),
            "structural_blocks": len(rows) - len(ok),
            "evaluated": len(ok),
            "gate_passes": passes,
            "gate_blocks": len(ok) - passes,
        }
        if ok:
            summary.update(
                {
                    "g_median": float(np.median([r["G"] for r in ok])),
                    "heldout_median": float(np.median([r["heldout"] for r in ok])),
                    "null_gap_median": float(np.median([r["null_gap"] for r in ok])),
                    "truth_median": float(np.median([r["truth"] for r in ok])),
                }
            )
        per_variant[variant[0]] = {"summary": summary, "rows": rows}
        print(f"  {name} / {variant[0]}: {json.dumps(summary)}")
    return per_variant


def _majority_verdict(summary: dict) -> str | None:
    if summary["evaluated"] == 0:
        return None
    return "pass" if summary["gate_passes"] * 2 >= summary["evaluated"] else "block"


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--p5", action="store_true",
        help="include the replayed frozen P5 continuum samples",
    )
    args = parser.parse_args()

    started = time.perf_counter()
    families: dict[str, list[tuple[str, np.ndarray, int]]] = {
        "uniform": uniform_orders(),
        "layered": layered_orders(),
        "crystal": [("crystal_bipartite", bipartite_perm(N_ELEMENTS), 0)],
    }
    if args.p5:
        print("== replaying frozen P5 continuum cells ==")
        families["p5_continuum"] = p5_orders()

    results: dict = {
        "n_elements": N_ELEMENTS,
        "pipeline_constants": {
            "max_targets": MAX_TARGETS, "min_targets": MIN_TARGETS,
            "train_c": TRAIN_C, "held_c": HELDOUT_C, "steps": STEPS,
            "restarts": RESTARTS, "min_cols": MIN_COLS,
            "truth_comparisons": TRUTH_COMPARISONS,
            "gate_heldout": GATE_HELDOUT, "gate_null_gap": GATE_NULL_GAP,
            "gate_truth": GATE_TRUTH,
        },
        "variants": [
            {
                "name": name, "selector": kind, "chain_count": count,
                "min_length": length, "selector_seed": sseed,
            }
            for name, kind, count, length, sseed in VARIANTS
        ],
        "families": {},
    }
    for name, orders in families.items():
        print(f"== {name} ({len(orders)} orders) ==")
        results["families"][name] = scan_family(name, orders)

    # Verdict, criterion fixed above: majority verdict per family per
    # variant, compared against the frozen selector's majority verdict.
    flips = []
    verdicts: dict[str, dict[str, str | None]] = {}
    for family, per_variant in results["families"].items():
        frozen_verdict = _majority_verdict(per_variant[FROZEN_VARIANT]["summary"])
        verdicts[family] = {}
        for variant_name, block in per_variant.items():
            verdict = _majority_verdict(block["summary"])
            verdicts[family][variant_name] = verdict
            if (
                verdict is not None
                and frozen_verdict is not None
                and verdict != frozen_verdict
            ):
                flips.append({"family": family, "variant": variant_name})
    results["verdict"] = {
        "majority_verdicts": verdicts,
        "flips_vs_frozen_selector": flips,
        "crystal_always_structurally_blocked": all(
            block["summary"]["evaluated"] == 0
            for block in results["families"]["crystal"].values()
        ),
    }
    results["status"] = (
        "descriptive reconnaissance; frozen gates and fit pipeline unchanged; "
        "only the chain-selection stage varied; no frozen artifact modified"
    )
    results["elapsed_seconds"] = round(time.perf_counter() - started, 1)

    RESULTS_PATH.write_text(
        json.dumps(results, indent=2) + "\n", encoding="utf-8"
    )
    print(f"\nverdict: {json.dumps(results['verdict'], indent=2)}")
    print(f"wrote {RESULTS_PATH}")


if __name__ == "__main__":
    main()
