"""Chain-selector robustness probe: does the verdict depend on the selector?

Paper B Section 11 lists "robustness of the verdict to the chain-selection
rule (number of chains, tick threshold, non-greedy extraction)" as an open
extension, and the P7 re-scope reconnaissance showed the greedy selector is
a real degree of freedom of the instrument (its N = 200-400 structural
blocks are greedy-selector properties, not order properties). This probe
asks the complementary question inside the instrument's operating window:
at N = 600, does the frozen PASS/BLOCK verdict move when the chain-selection
stage -- and only that stage -- is varied?

Design (all choices fixed before any result was read; descriptive
reconnaissance, not a preregistered experiment; the frozen gates and the
frozen fit pipeline are used unchanged):

- Families, regenerated exactly from their committed constructions:
  * uniform 2D orders (the beta = 0 positive control; exact coordinates
    x = i - pi_i), fresh descriptive seeds 200-219 disjoint from every
    frozen seed range;
  * the eight frozen P6a layered cells (hard negatives), confirmatory
    seeds 100-104, via the frozen construction (balanced layered
    permutation + windowed transpositions, window 60, jitter seed
    10000 + seed);
  * the bipartite crystal (structural negative);
  * optionally (--p5) the frozen P5 continuum cells (beta in {2, 8},
    seeds 100-102), regenerated by the trajectory-equivalent accelerated
    replay and provenance-checked against the frozen chain mean action.
- Selector variants (the frozen protocol is greedy 6 chains x >= 25 ticks):
  * greedy at (4, 25), (8, 25), (6, 20), (6, 30) -- chain count and tick
    threshold, one axis at a time around the frozen point;
  * the frozen greedy (6, 25) itself, as the anchor row;
  * a non-greedy ANCHORED selector at (6, 25), three selector seeds:
    each chain is the longest chain through a uniformly chosen anchor
    element (not the globally longest chain), so chain placement is
    randomized and greedy whole-chain consumption is broken.
- Everything downstream of chain selection is the frozen pipeline
  verbatim (bracketed targets, profiles, dissimilarity, fit, gates:
  held-out 0.10 / null gap 0.10 / truth 0.40, truth from 8000 pair
  comparisons as in p3_dynamics.py).

Verdict criterion, fixed in advance: a variant FLIPS a family if the
family's majority verdict among numerically valid runs (pass vs block)
differs from the frozen selector's majority verdict. Structural blocks are
reported separately as operability changes, not verdict flips (the
instrument gives no verdict there) -- the same separation the P7 probe used.

Writes the tracked table docs/selector_robustness_probe_results.json
(deterministic seeds; audit-trail convention of PR #8).

Usage:
    python selector_robustness_probe.py [--p5]
"""

